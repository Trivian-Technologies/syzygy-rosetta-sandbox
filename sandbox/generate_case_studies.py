    print(f"\n{'='*60}")
    print(f"  CASE STUDY GENERATION COMPLETE")
    print(f"{'='*60}")
    print(f"\n  Statistics:")
    print(f"    🚫 Escalated: {total_escalated}")
    print(f"    ⚠️  Rewritten: {total_rewritten}")
