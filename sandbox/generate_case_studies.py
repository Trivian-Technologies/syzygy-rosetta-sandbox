# Fix Windows console encoding
if sys.platform == 'win32':
    sys.stdout.reconfigure(encoding='utf-8', errors='replace')

ROSETTA_URL = settings.rosetta_url or "http://localhost:8000"
CASE_STUDIES_DIR = Path(__file__).parent / "case_studies"
LOGS_DIR = Path(__file__).parent.parent / "logs"

def evaluate_with_rosetta(content: str, industry: str) -> Dict:
    """Send content to Rosetta for evaluation"""
    try:
        response = requests.post(
            f"{ROSETTA_URL}/evaluate",
            json={
                "input": content,
                "context": {
                    "environment": "production",
                    "industry": industry
                }
            },
            timeout=10
        )
        response.raise_for_status()
        return response.json()
    except Exception as e:
        return {"decision": "error", "error": str(e)}

def generate_case_study_markdown(scenario: Dict, eval_result: Dict, industry: str) -> str:
    """Generate markdown for a single case study"""
    
    decision_icon = {
        "allow": "✅ ALLOW",
        "rewrite": "⚠️ REWRITE", 
        "escalate": "🚫 ESCALATE"
    }.get(eval_result.get("decision", "unknown"), "❓ UNKNOWN")
    
    md = f"""# Case Study: {scenario['name']}

**Industry:** {industry.upper()}  
**Scenario ID:** {scenario['id']}  
**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

---

## Scenario Overview

{scenario.get('description', 'A test scenario demonstrating Rosetta governance.')}

---

## Scenario A: Without Rosetta (Raw Agent Output)

### User Input
> {scenario['input']}

### Agent Response (UNGOVERNED)
```
{scenario['raw_response']}
```

**⚠️ Risk Assessment:** This output contains potentially harmful content that would reach end users without any governance layer.

---

## Scenario B: With Rosetta Governance

### Rosetta Evaluation Result

| Field | Value |
|-------|-------|
| **Decision** | {decision_icon} |
| **Risk Score** | `{eval_result.get('risk_score', 'N/A')}` |
| **Confidence** | `{eval_result.get('confidence', 'N/A')}` |
| **Violations** | `{', '.join(eval_result.get('violations', [])) or 'None'}` |
| **Timestamp** | `{eval_result.get('timestamp', 'N/A')}` |

### Reasoning
> {eval_result.get('reasoning', 'N/A')}

"""
    
    if eval_result.get('rewrite'):
        md += f"""### Suggested Rewrite
> {eval_result.get('rewrite')}

"""
    
    md += f"""### Field Notes
```
{json.dumps(eval_result.get('field_notes', []), indent=2)}
```

---

## Evaluation Log Entry

```json
""" + json.dumps({
        "timestamp": eval_result.get("timestamp"),
        "input": scenario['raw_response'][:200] + "...",
        "decision": eval_result.get("decision"),
        "risk_score": eval_result.get("risk_score"),
        "confidence": eval_result.get("confidence"),
        "violations": eval_result.get("violations"),
        "rewrite": eval_result.get("rewrite"),
        "context": {
            "user_id": None,
            "environment": "production",
            "industry": industry
        }
    }, indent=2) + """
```

---

## Conclusion

"""

    decision = eval_result.get("decision", "unknown")
    if decision == "escalate":
        md += """**Result:** Rosetta successfully **ESCALATED** this high-risk output for human review. 
The raw agent response would have provided harmful instructions to the user. 
With Rosetta governance, this output is blocked and flagged for review before reaching the user.
"""
    elif decision == "rewrite":
        md += """**Result:** Rosetta successfully **FLAGGED** this output for rewriting.
The raw agent response contained suspicious or potentially harmful content.
With Rosetta governance, a safer rewrite is suggested before the output reaches the user.
"""
    else:
        md += """**Result:** Rosetta evaluated this output and determined it was safe to proceed.
Note: This case study demonstrates that even benign-looking content undergoes governance evaluation.
"""

    md += """
---

*Case study generated by Syzygy Rosetta Sandbox*
"""
    
    return md

